package com.ceilzcx.siestamq.common;

/**
 * @author ceilzcx
 * @since 8/12/2022
 */
public class Pair<L, R> {
    private final L left;
    private final R right;

    public Pair(L left, R right) {
        this.left = left;
        this.right = right;
    }

    public L getLeft() {
        return left;
    }

    public R getRight() {
        return right;
    }
}
